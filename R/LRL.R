#' @title Low-Rank Linear regression for matrix responses.
#' @description Low-Rank Linear regression for matrix responses.
#' @param x The coefficient matrix in regression.
#' @param y The list consisting of  samples' responses.
#' @param lambda Penalty factor.
#' @param epsilon Iteration limit.
#' @return a list consisting of parameter estimation.
#' @examples
#' \dontrun{
#' data(xy)
#'x<-xy$X
#'y<-xy$Y
#'B_hat<-LRL(x,y)
#' }
#' @export
LRL<-function(x,y,lambda=1,epsilon=0.1){
  Sc<-function(B1,B0,a1){
    sc<-list()
    for (i in 1:4){
      sc<-c(sc,list(B1[[i]]+(a1-1)/a1*(B1[[i]]-B0[[i]])))
    }
    return(sc)
  }

  Gradient<-function(x,y,S,i){
    n<-dim(x)[1]
    p<-matrix(0,64,64)
    for ( j in 1:n){
      p<-p+x[j,i]^2*S[[i]]-x[j,i]*(y[[j]]-x[j,1]*S[[1]]-x[j,2]*S[[2]]-x[j,3]*S[[3]]
                                   -x[j,4]*S[[4]]+x[j,i]*S[[i]])
    }
    p/n
  }
  QB<-function(B_hat,x,y,lambda=0){
    v<-list()
    n<-dim(x)[1]
    mse<-numeric(n)
    for (i in 1:n){
      a<-y[[i]]-x[i,1]*B_hat[[1]]-x[i,2]*B_hat[[2]]-x[i,3]*B_hat[[3]]-x[i,4]*B_hat[[4]]
      mse[i]<-sum(a^2)
    }
    v$MSE<-mean(mse)/2
    m<-length(B_hat)
    d<-numeric(m)
    for ( i in 1:m){
      d[i]<-sum(svd(B_hat[[i]])$d)
    }
    v$Bnorm<-lambda*sum(d)
    v$QB<- v$MSE+v$Bnorm
    v$norm<-sum(d)
    return(v)
  }
  delta<-length(y)/max(svd(t(x)%*%x)$d)
  B1<-B0<-list(matrix(0,64,64),matrix(0,64,64),
               matrix(0,64,64),matrix(0,64,64))
  a0<-0;a1<-1;C<-1
  while (isTRUE(C>epsilon)){
    S1<-Sc(B1,B0,a1)
    B0<-B1
    for (i in 1:4){
      A<-S1[[i]]-delta*Gradient(x,y,S1,i)
      sv<-svd(A)
      b<-sv$d-lambda*delta
      b<-(abs(b)+b)/2
      Bp<-sv$u%*%diag(b)%*%t(sv$v)
      B1[[i]]<-Bp
    }
    a1<-(1+sqrt(1+(2*a1)^2))/2
    C<-abs(QB(B1,x,y,lambda)$QB-QB(B0,x,y,lambda)$QB)

  }
  return(B1)
}
